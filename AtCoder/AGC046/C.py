M = 310
M2 = M * M

S, K = input().split()
K = int(K)

n = 0
N = []
for c in S:
    if c == "1":
        n += 1
    else:
        N.append(n)
        n = 0

N.append(n)
N.reverse()

dp = [0] * M2
dp[0] = 1

for i, n in enumerate(N):
    dp_next = [0] * M2

    if i > 0:
        for h in range(M):
            s = 0
            for w in range(M):
                i = h * M + w
                dp_next[i] = s
                s += dp[i]

    for w in range(M):
        S = [0]
        for h in range(M):
            i = h * M + w
            S.append(S[-1] + dp[i])
            dp_next[i] += S[h + 1] - S[max(0, h + 1 - (n + 1))]

    dp = dp_next

    print(n)
    for h in range(5):
        print(dp[h * M : h * M + M][:5])

print()

